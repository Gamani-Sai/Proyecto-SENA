package Entidad;

/**
 *
 * @author hp
 */
public class entidadProfesores {

    private String Identificacion = "";
    private String Nombre = "";
    private String Apellido = "";
    private String Direccion = "";
    private String Telefono = "";
    private String huella = "";
    private String Fecha_Nacimiento = "";
    private String Perfil_Profesional = "";
    private String Estado ="";

    /**
     * @return the Identificacion
     */
    public String getIdentificacion() {
        return Identificacion;
    }

    /**
     * @param Identificacion the Identificacion to set
     */
    public void setIdentificacion(String Identificacion) {
        this.Identificacion = Identificacion;
    }

    /**
     * @return the Nombre
     */
    public String getNombre() {
        return Nombre;
    }

    /**
     * @param Nombre the Nombre to set
     */
    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    /**
     * @return the Apellido
     */
    public String getApellido() {
        return Apellido;
    }

    /**
     * @param Apellido the Apellido to set
     */
    public void setApellido(String Apellido) {
        this.Apellido = Apellido;
    }

    /**
     * @return the Direccion
     */
    public String getDireccion() {
        return Direccion;
    }

    /**
     * @param Direccion the Direccion to set
     */
    public void setDireccion(String Direccion) {
        this.Direccion = Direccion;
    }

    /**
     * @return the Telefono
     */
    public String getTelefono() {
        return Telefono;
    }

    /**
     * @param Telefono the Telefono to set
     */
    public void setTelefono(String Telefono) {
        this.Telefono = Telefono;
    }

    /**
     * @return the huella
     */
    public String getHuella() {
        return huella;
    }

    /**
     * @param huella the huella to set
     */
    public void setHuella(String huella) {
        this.huella = huella;
    }

    /**
     * @return the Fecha_Nacimiento
     */
    public String getFecha_Nacimiento() {
        return Fecha_Nacimiento;
    }

    /**
     * @param Fecha_Nacimiento the Fecha_Nacimiento to set
     */
    public void setFecha_Nacimiento(String Fecha_Nacimiento) {
        this.Fecha_Nacimiento = Fecha_Nacimiento;
    }

    /**
     * @return the Perfil_Profesional
     */
    public String getPerfil_Profesional() {
        return Perfil_Profesional;
    }

    /**
     * @param Perfil_Profesional the Perfil_Profesional to set
     */
    public void setPerfil_Profesional(String Perfil_Profesional) {
        this.Perfil_Profesional = Perfil_Profesional;
    }

    /**
     * @return the Estado
     */
    public String getEstado() {
        return Estado;
    }

    /**
     * @param Estado the Estado to set
     */
    public void setEstado(String Estado) {
        this.Estado = Estado;
    }
}
